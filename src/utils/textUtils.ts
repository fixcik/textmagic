/**
 * Определяет язык текста по наличию кириллицы
 * @param text Текст для анализа
 * @returns 'ru' если текст на русском, 'en' если на английском или другом языке
 */
export const detectLanguage = (text: string): 'ru' | 'en' => {
  // Регулярное выражение для поиска кириллических символов
  const cyrillicPattern = /[а-яА-ЯёЁ]/;

  // Если в тексте есть кириллица, считаем его русским
  if (cyrillicPattern.test(text)) {
    return 'ru';
  }

  // По умолчанию считаем текст английским
  return 'en';
}; 